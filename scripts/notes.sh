#!/usr/bin/env bash

folder=$HOME/notes/

newnote () { \
  dir="$(command ls -d "$folder" "$folder"*/ | dmenu -l 5 -p 'Choose directory: ')"|| exit 0
  : "${dir:=$folder}"
  name="$(echo "" | dmenu -sb "#a3be8c" -nf "#d8dee9" -p "Enter a name: " <&-)" || exit 0
  : "${name:=$(date +%F_%H-%M-%S)}"
  setsid -f alacritty -e nvim $dir$name".md" >/dev/null 2>&1
}

selected () { \
  choice=$(
    echo -e "New\n$(find $folder -type f -printf '%T@ %P\n' | sort -nr | cut -d' ' -f2-)" | dmenu -l 5 -p "Choose note or create new: "
  )
  case $choice in
    New) newnote ;;
    *.md) setsid -f alacritty -e nvim "$folder$choice" >/dev/null 2>&1 ;;
    *) exit ;;
  esac
}

selected
